char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	unsigned int	i;

	i = 0;
	while (src[i] != 0 && i < n)
	{
		dest[i] = src[i];
		i += 1;
	}
	while (i < n)
	{
		dest[i] = 0;
		i += 1;
	}
	return (dest);
}
