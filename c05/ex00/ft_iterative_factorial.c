int	ft_iterative_factorial(int nb)
{
	int	i;
	int	x;

	if (nb < 0)
		return (0);
	i = 1;
	x = 1;
	while (i <= nb)
	{
		x *= i;
		i += 1;
	}
	return (x);
}
